def map_bank_category(bank_category: str | None) -> dict | None:
    if not bank_category:
        return None

    raw = bank_category.strip()
    if not raw:
        return None

    normalized = raw.lower().replace("ё", "е")
    category_name = SBER_CATEGORY_ALIASES.get(normalized, raw)

    return {
        "category": category_name,
        "confidence": 0.90,
        "source": "bank",
    }
